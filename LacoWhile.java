package curso;

public class LacoWhile {

	public static void main(String[] args) {
		
		// laco while - loop
		//inicializacao
		// Incremento
		// Sintaxe -  while (condicao) { ....}
        // exemplo
		
		// incializacao
		int i = 1;
		// criterio da parada
		while (i <= 5) {
			// incremento
			i++; // i = i + 1
			System.out.println(i);
		}
		// System.out.println("DEPOIS DO LACO");
	}

}
