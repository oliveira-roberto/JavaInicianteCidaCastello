public class Variaveis_02 {
	
	public static void main(String[] args) {
		
		//declaracao de vatriaveis
		//<tipo> <nome da variavel>;
		//<tipo> <nome da variavel> = <valor;
		//tipo inteiro
		int a; //declaracao de variavel
		a = 5;
		//declarando e inicializando a variavel
		int b = 3;
		
		System.out.println(a + b);
		//declarar a variavel que vai ter a soma a + b
		int soma = a + b;
		System.out.println(soma);
		
	}

}
