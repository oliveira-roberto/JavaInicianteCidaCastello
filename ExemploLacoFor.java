package curso;

public class ExemploLacoFor {
	
	public static void main(String[] args) {
		
		// laco for
		// variavel e inicializacao - contador
		// incremento
		// criterio de parada
		
		// sintaxe for (condicao inicial; criterio de parada; criterio) {.......; ...}
		
		int i;
		
		for (i = 0; i <= 10; i++) {
			System.out.println(i);
		}
	}

}
